# Recebe o primeiro valor inteiro do usuário e armazena na variável 'a'
valor1 = int(input())

# Recebe o segundo valor inteiro do usuário e armazena na variável 'b' 
valor2 = int(input())

# Calcula o produto (multiplicação) dos valores 'a' e 'b' e armazena em 'PROD'
